    private void txtStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtStockMouseClicked
       int a=Integer.parseInt(txtCantidad.getText());
       int b=500;
       if(a>=10 && a<=500)
       {
           int result = b-a;
           String r = Integer.toString(result);
           txtStock.setText(r);
           JOptionPane.showMessageDialog(null,"Cantidad restante del producto es de " + result);
       }else
           if(a>=0 && a<10)
           {
             JOptionPane.showMessageDialog(null,"El Stock del producto no puede ser menor que 10");  
           }
       else
          JOptionPane.showMessageDialog(null,"Esta cantidad sobre pasa el Stock del producto");     
                  
        
    }//GEN-LAST:event_txtStockMouseClicked

