    public void mostrar(){
    
        String matris[][]=new String[lista.size()][9];
        
        for(int i = 0; i < lista.size(); i++){
            
            matris[i][0]=lista.get(i).getCod_producto();
            matris[i][1]=lista.get(i).getNombre();
            matris[i][2]=lista.get(i).getModelo();
            matris[i][3]=lista.get(i).getUnidades();
            matris[i][4]=lista.get(i).getPrecio_costo();
            matris[i][5]=lista.get(i).getPrecio_venta();
            matris[i][6]=lista.get(i).getTotal();
            matris[i][7]=lista.get(i).getStock();
            matris[i][8]=lista.get(i).getMargen();
            
        
            
        }
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
          matris,
            new String [] {
                "Cod_Producto", "Nombre", "Modelo", "Unidades", "Precio_Costo", "Precio_Venta", "Total", "Stock", "Margen"
            }
        ));
        
    }
    
    
    
    
    
    
    
    
    
    
    
